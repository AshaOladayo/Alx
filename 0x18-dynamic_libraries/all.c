#include <stdio.h>
/**
 * add - add two integers
 * @: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * mul- add two integers
 * @: first int
 * @b: second int
 * Return: addition
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div- add two integers
 * @: first int
 * @b: second int
 * Return: addition
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * sub - subtract two integers
 * @: first int
 * @b: second int
 * Return: addition
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mod- add two integers
 * @: first int
 * @b: second int
 * Return: addition
 */
int mod(int a, int b)
{
	return (a % b);
}
